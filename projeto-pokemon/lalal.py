import random
from sqlalchemy import create_engine, Column, Integer, String
from sqlalchemy.orm import sessionmaker, declarative_base

# Configuração do banco de dados
engine = create_engine('sqlite:///biblioteca.db')
Session = sessionmaker(bind=engine)
session = Session()

Base = declarative_base()

class Pokemon(Base):
    _tablename_ = "pokemons"

    id_pokemon = Column("id", Integer, primary_key=True, autoincrement=True)
    hp = Column("hp", Integer)
    ataque = Column("ataque", Integer)
    velocidade = Column("velocidade", Integer)
    tipo = Column("tipo", String)
    nome = Column("nome", String)
    regiao = Column("regiao", String)

    def _repr_(self):
        return f'(Nome: {self.nome}) | Vida: {self.hp} | Ataque: {self.ataque} | Velocidade: {self.velocidade} | Tipo: {self.tipo} | Região: {self.regiao}\n'

    def atacar(self, outro_pokemon, tipo_ataque):
        if tipo_ataque == "Ataque Rápido":
            dano = self.ataque * 0.8
        elif tipo_ataque == "Ataque Forte":
            dano = self.ataque * 1.2
        elif tipo_ataque == "Ataque Crítico":
            dano = self.ataque * 1.5
        else:
            dano = self.ataque

        dano_final = int(dano + random.randint(-5, 5))
        outro_pokemon.hp -= dano_final
        return dano_final, tipo_ataque

