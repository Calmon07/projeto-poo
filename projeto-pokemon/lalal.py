def batalhar(pokemon1, pokemon2):
    print(f"Iniciando a batalha entre {pokemon1.nome} e {pokemon2.nome}!\n")
    batalha_ativa = True

    while pokemon1.hp > 0 and pokemon2.hp > 0 and batalha_ativa:
        print(f"\n{pokemon1.nome}: {pokemon1.hp} HP | {pokemon2.nome}: {pokemon2.hp} HP")
        print(f"\n{pokemon1.nome} {pokemon1.representacao_ascii()}")
        print(f"{pokemon2.nome} {pokemon2.representacao_ascii()}")
        
        print("\nEscolha sua ação:")
        print("1 - Ataque Rápido")
        print("2 - Ataque Forte")
        print("3 - Ataque Crítico")
        print("4 - Fugir")

        escolha = input("Digite o número da sua escolha: ")

        if escolha == "4":
            print(f"{pokemon1.nome} decidiu fugir da batalha!")
            batalha_ativa = False
            break

        if escolha == "1":
            tipo_ataque = "Ataque Rápido"
        elif escolha == "2":
            tipo_ataque = "Ataque Forte"
        elif escolha == "3":
            tipo_ataque = "Ataque Crítico"
        else:
            print("Escolha inválida, tente novamente.")
            continue

        dano1, tipo_ataque1 = pokemon1.atacar(pokemon2, tipo_ataque)
        print(f"\n{pokemon1.nome} usou {tipo_ataque1} e causou {dano1} de dano em {pokemon2.nome}.")
        
        if pokemon2.hp <= 0:
            print(f"{pokemon2.nome} foi derrotado! {pokemon1.nome} venceu a batalha!\n")
            break

        tipo_ataque2 = "Ataque Forte"
        dano2, tipo_ataque2 = pokemon2.atacar(pokemon1, tipo_ataque2)
        print(f"{pokemon2.nome} usou {tipo_ataque2} e causou {dano2} de dano em {pokemon1.nome}.")
        
        if pokemon1.hp <= 0:
            print(f"{pokemon1.nome} foi derrotado! {pokemon2.nome} venceu a batalha!\n")
            break

def criar_pokemon(nome, hp, ataque, velocidade, tipo, regiao):
    pokemon = Pokemon(nome=nome, hp=hp, ataque=ataque, velocidade=velocidade, tipo=tipo, regiao=regiao)
    session.add(pokemon)
    session.commit()
    return pokemon

